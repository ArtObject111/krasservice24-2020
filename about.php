           <h3>О технологии Walnut shell blasting</h3>
         <p>Технология BMW. Как всем известно, двигатели с непосредственным впрыском топлива, которыми оснащены автомобили марки BMW N-серии, имеют проблему загрязнения впускного коллектора и впускных клапанов углеродистыми отложениями.</p>
         <p>Топливо поступает прямиком в цилиндры, а не распыляется перед впускными клапанами. Масло садится на клапана, затем коксуется из-за высоких температур и скапливается там до бесприличных размеров.</p>
        <p>Решить данную проблему помогает чистка методом мягкого бластинга с применением абразива из скорлупы грецкого ореха (Walnut shell blasting). При попадании в цилиндр, частички скорлупы не принесут никакого вреда, а просто сгорят там. Не повреждает поверхность. На выходе вы получаете абсолютно чистый впуск. Опасность данного загрязнения (нагара) грозит откалыванию частиц (масляного сталактита) и попадания его в цилиндры двигателя.  А это в свою очередь может привести к потери мощности и не стабильной работе двигателя, вплоть до полного его отказа работать (в самых «запущенных» случаях, когда количество нагара уже действительно критично)</p>
        <p>Проведение данной процедуры возможна почти на любых моделях авто с дизельными или бензиновыми двигателями без демонтажа и разборки ГБЦ. Как показывает практика, после проведения чистки впускной системы: вихревых заслонок изменения геометрии впускного коллектора, системы ЕГР и камер впускных клапанов, двигатель начинает работать ровно (больше выражается при холодном пуске), улучшается динамика, тяга, снижается расход топлива. И наконец самым важным результатом данной процедуры – продление  жизни сердца вашего автомобиля от 80000 и до 100000км.  Проведение данной процедуры  желательна через каждые 100000км на всех современных дизельных двигателях класса евро 4 и выше. Или перед отключением системы ЕГР и прошивки ДВС.</p>
